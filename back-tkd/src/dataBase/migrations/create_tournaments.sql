CREATE TABLE TOURNAMENTS (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(255),
    DATE DATE,
    HOURS VARCHAR(255),
    ADDRESS VARCHAR(255),
    COST VARCHAR(255),
    IND_TUL BOOL,
    IND_FIGHT BOOL,
    IND_ROTURA_HABILIDAD BOOL,
    IND_ROTURA_POTENCIA BOOL,
    TEAM_TUL BOOL,
    TEAM_FIGHT BOOL,
    TEAM_DEFENSA_PERSONAL BOOL,
    TEAM_TUL_APLICADO BOOL,
    CAT_INFANTILES BOOL,
    CAT_CADETES_11_13 BOOL,
    CAT_JUVENIL_14_17 BOOL,
    CAT_ADULTO_18_34 BOOL,
    CAT_VETERANO_PLATA_35_44 BOOL,
    CAT_VETERANO_ORO_45 BOOL,
    AFICHE VARCHAR(255),
);