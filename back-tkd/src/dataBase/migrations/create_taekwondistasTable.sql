CREATE TABLE TAEKWONDISTAS (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    NAME VARCHAR(255),
    LASTNAME VARCHAR(255),
    DNI VARCHAR(20),
    BIRTHDATE DATE,
    ADDRESS VARCHAR(255),
    CATEGORY VARCHAR(20),
    SABOM VARCHAR(255),
    WEIGHT FLOAT,
    HEIGHT FLOAT,
    GENDER VARCHAR(20),
    NACIONALITY VARCHAR(255),
    PICTURE VARCHAR(255),
    PHONE VARCHAR(20),
    EMAIL VARCHAR(255),
    CONTACT_NAME VARCHAR(255),
    CONTACT_LASTNAME VARCHAR(255),
    CONTACT_RELATIONSHIP VARCHAR(50),
    CONTACT_PHONE VARCHAR(20)
);